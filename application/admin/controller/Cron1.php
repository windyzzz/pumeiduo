                'user_name' => $v['user_name'],
                'true_name' => $v['true_name'],
                'mobile' => $v['mobile'],
                'sex' => $v['sex'],
                'id_cart' => $v['id_card'],
                'birthday' => $v['birthday'],
                'reg_time' => strtotime($v['reg_date'] . ' ' . $v['reg_time']),
                'referee_user_name' => $v['referee_user_name'],
                'distribut_level' => 2,
